from task_4 import User
from json import load


class NameError(Exception):  # —Å–æ–∑–¥–∞–Ω–∏–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
    pass


class LevelError(Exception):  # —Å–æ–∑–¥–∞–Ω–∏–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
    pass


class Repo():
    users = set()

    @staticmethod
    def load_users(path):
        with open(path, encoding='utf-8') as f:
            data = load(f)
        for level, value in data.items():
            for id_user, name in value.items():
                Repo.users.add(User(id_user, name, level))

    @staticmethod
    def check_login(name):
        try:
            for user in Repo.users:
                if user.name == name:
                    return f'{name} –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–π–¥–µ–Ω!'
            else:
                raise NameError  # –ø–æ–¥–Ω—è—Ç–∏–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
        except NameError:  # –æ—Ç–ª–æ–≤ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
            return '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω'  # –æ–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ

    @staticmethod
    def create_user(id_user, name, level):
        try:
            if level > 3:
                raise LevelError  # –ø–æ–¥–Ω—è—Ç–∏–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
        except LevelError:  # –æ—Ç–ª–æ–≤ –∏—Å–∫–ª—é—á–µ–Ω–∏—è
            return '–û—à–∏–±–∫–∞ —É—Ä–æ–≤–Ω—è'  # –æ–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ
        else:
            Repo.users.add(User(id_user, name, level))


if __name__ == '__main__':
    repo = Repo()
    repo.load_users('users.json')
    print(*repo.users, sep='\n')
