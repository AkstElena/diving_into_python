class Rectangle:
    def __init__(self, *args):
        self.__length = args[0]
        self.__width = self.__length if len(args) == 1 else args[1]

    def area(self):
        return self.__length * self.__width

    def perimeter(self):
        return 2 * (self.__length + self.__width)